    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        if (cbVideos.getSelectedIndex() != 0) {
            VideoService.eliminarRegistro((((String) cbVideos.getSelectedItem()).split(","))[0]);
            cbVideos.removeAllItems();
            cbVideos.addItem("Elija una video...");
            addItems();
            JOptionPane.showMessageDialog(rootPane, "Se ha eliminado el registro!");
            return;
        } else {
            JOptionPane.showMessageDialog(rootPane, "No se ha seleccionado un registro!");
            return;
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

