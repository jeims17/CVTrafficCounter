    private void addItems() {
        String item;
        Collection<Video> videos = VideoService.getVideos();
        
        for (Video video : videos) {
            item = video.getFilePath() + "," + video.getCiudad() + "," + video.getCalle();
            cbVideos.addItem(item);
        }
    }
    
